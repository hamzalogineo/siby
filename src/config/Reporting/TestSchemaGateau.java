    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        
        String moule = "" ;
        moule = this.jComboBox1.getSelectedItem().toString().replaceAll("'", "''").trim() ;
        if(moule.isEmpty() || moule.equalsIgnoreCase("CHOISIR MOULE")){
            JOptionPane.showMessageDialog(null, "CHOISIR UN MOULE POUR LA COMMANDE") ;
        }else{
            /*
            M R
            M TR
            */
        if(moule.equalsIgnoreCase("M R")) {
            
            this.mr.setVisible(true) ;
            this.mt.setVisible(false);
            
            this.l1.setVisible(true);
            this.l5.setVisible(true);
        
            this.l2.setVisible(false);
            this.l3.setVisible(false);
            this.l4.setVisible(false);
            
        }else if(moule.equalsIgnoreCase("M TR")){
            
            this.mr.setVisible(false) ;
            this.mt.setVisible(true);
            
            this.l1.setVisible(true);
            this.l5.setVisible(true);
        
            this.l6.setVisible(false);
            this.l7.setVisible(false);
            this.l8.setVisible(false);
            
        }   
            
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

