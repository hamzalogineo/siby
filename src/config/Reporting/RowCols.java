package config.Reporting;

/**
 *
 * @author HAMZA
 */
public class RowCols {
    int row ;
    int cols ;
    
    public RowCols(Integer row , Integer cols){
        this.row = row ;
        this.cols = cols ;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCols() {
        return cols;
    }

    public void setCols(int cols) {
        this.cols = cols;
    }
    
    
    
    
}
